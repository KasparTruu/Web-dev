<script>
export default {
name: 'Myself',
mounted() {
    // Animate skill progress bars
    this.animateSkills();
    
    // Setup intersection observer for skills
    this.setupSkillsObserver();
},
methods: {
    animateSkills() {
    const skillBars = document.querySelectorAll('.skill-progress');
    skillBars.forEach(bar => {
        const targetWidth = bar.getAttribute('data-width');
        setTimeout(() => {
        bar.style.width = targetWidth;
        bar.style.transition = 'width 1.5s ease-in-out';
        }, 300);
    });
    },
    setupSkillsObserver() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
        if (entry.isIntersecting) {
            const skillCards = entry.target.querySelectorAll('.skill-card');
            skillCards.forEach((card, index) => {
            setTimeout(() => {
                card.classList.add('animate-skill-card');
            }, index * 150);
            });
            
            const competencyCards = entry.target.querySelectorAll('.competency-card');
            competencyCards.forEach((card, index) => {
            setTimeout(() => {
                card.classList.add('animate-competency');
            }, index * 150);
            });
            
            observer.unobserve(entry.target);
        }
        });
    }, { threshold: 0.1 });
    
    const skillsSection = document.querySelector('.container');
    if (skillsSection) {
        observer.observe(skillsSection);
    }
    }
}
}
</script>

